     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $ids = Type_Product::where('type_id', '=', '19')->get('product_id');
        $query = Product::query();
        $count = 0;
        foreach($ids as $id){
            if($count === 0){
                $query->where('id', '=', $id->product_id);
            }
            else{
                $query->orWhere('id', '=', $id->product_id);
            }
            $count++;
        }
        $data['products'] = $query->get();
        return view('dashboard.calcas',$data);
    }

